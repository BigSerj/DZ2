package Appliances;

public class MeatGrinders extends CookingTechniks{
    private String kindOfKnives;
    private int numberOfKnives;
    private int weight;
    private int height;
    private int width;
    private int depth;


    public String getKindOfKnives() {
        return kindOfKnives;
    }

    public void setKindOfKnives(String kindOfKnives) {
        this.kindOfKnives = kindOfKnives;
    }

    public int getNumberOfKnives() {
        return numberOfKnives;
    }

    public void setNumberOfKnives(int numberOfKnives) {
        this.numberOfKnives = numberOfKnives;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getDepth() {
        return depth;
    }

    public void setDepth(int depth) {
        this.depth = depth;
    }
}
