package Appliances;

public class Mixers extends CookingTechniks{
    private int numberOfNozzles;
    private int weight;
    private int height;
    private int width;
    private int depth;



    public int getNumberOfNozzles() {
        return numberOfNozzles;
    }

    public void setNumberOfNozzles(int numberOfNozzles) {
        this.numberOfNozzles = numberOfNozzles;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getDepth() {
        return depth;
    }

    public void setDepth(int depth) {
        this.depth = depth;
    }
}
